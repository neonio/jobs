import UIKit
struct IconFont {
    static func font(size: CGFloat) -> UIFont {
        return UIFont(name: "iconfont", size: size)!
    }
    static let add = "\u{e600}"
    static let location = "\u{e613}"
    static let article = "\u{e60c}"
    static let list = "\u{e626}"
    static let more = "\u{e63d}"
}
